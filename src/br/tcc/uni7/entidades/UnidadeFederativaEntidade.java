package br.tcc.uni7.entidades;

public class UnidadeFederativaEntidade {
	private Integer codigoUF;
	private Integer codigoPais;
	private String descricaoUF;
	private String siglaUF;
	/**
	 * @return the codigoUF
	 */
	public Integer getCodigoUF() {
		return codigoUF;
	}
	/**
	 * @param codigoUF the codigoUF to set
	 */
	public void setCodigoUF(Integer codigoUF) {
		this.codigoUF = codigoUF;
	}
	/**
	 * @return the codigoPais
	 */
	public Integer getCodigoPais() {
		return codigoPais;
	}
	/**
	 * @param codigoPais the codigoPais to set
	 */
	public void setCodigoPais(Integer codigoPais) {
		this.codigoPais = codigoPais;
	}
	/**
	 * @return the descricaoUF
	 */
	public String getDescricaoUF() {
		return descricaoUF;
	}
	/**
	 * @param descricaoUF the descricaoUF to set
	 */
	public void setDescricaoUF(String descricaUF) {
		this.descricaoUF = descricaUF;
	}
	/**
	 * @return the siglaUF
	 */
	public String getSiglaUF() {
		return siglaUF;
	}
	/**
	 * @param siglaUF the siglaUF to set
	 */
	public void setSiglaUF(String siglaUF) {
		this.siglaUF = siglaUF;
	}
	
	
}
