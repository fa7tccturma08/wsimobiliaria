package br.tcc.uni7.entidades;

public class CidadeEntidade {
	private Integer codigoCidade;
	private Integer codigoEstado;
	private String descricaoCidade;
	/**
	 * @return the codigoCidade
	 */
	public Integer getCodigoCidade() {
		return codigoCidade;
	}
	/**
	 * @param codigoCidade the codigoCidade to set
	 */
	public void setCodigoCidade(Integer codigoCidade) {
		this.codigoCidade = codigoCidade;
	}
	/**
	 * @return the codigoEstado
	 */
	public Integer getCodigoEstado() {
		return codigoEstado;
	}
	/**
	 * @param codigoEstado the codigoEstado to set
	 */
	public void setCodigoEstado(Integer codigoEstado) {
		this.codigoEstado = codigoEstado;
	}
	/**
	 * @return the descricaoCidade
	 */
	public String getDescricaoCidade() {
		return descricaoCidade;
	}
	/**
	 * @param descricaoCidade the descricaoCidade to set
	 */
	public void setDescricaoCidade(String descricaoCidade) {
		this.descricaoCidade = descricaoCidade;
	}
	
	
}
