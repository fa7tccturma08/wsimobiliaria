package br.tcc.uni7.entidades;

public class TipoImovelEntidade {
	private Integer codigoTipoImovel;
	private String descricaoTipoImovel;

	/**
	 * @return the codigoTipoImovel
	 */
	public Integer getCodigoTipoImovel() {
		return codigoTipoImovel;
	}

	/**
	 * @param codigoTipoImovel
	 *            the codigoTipoImovel to set
	 */
	public void setCodigoTipoImovel(Integer codigoTipoImovel) {
		this.codigoTipoImovel = codigoTipoImovel;
	}

	/**
	 * @return the descricaoTipoImovel
	 */
	public String getDescricaoTipoImovel() {
		return descricaoTipoImovel;
	}

	/**
	 * @param descricaoTipoImovel
	 *            the descricaoTipoImovel to set
	 */
	public void setDescricaoTipoImovel(String descricaoTipoImovel) {
		this.descricaoTipoImovel = descricaoTipoImovel;
	}
}
