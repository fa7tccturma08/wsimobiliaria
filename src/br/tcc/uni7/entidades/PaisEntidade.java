package br.tcc.uni7.entidades;

public class PaisEntidade {
	private Integer codigoPais;
	private String descricaoPais;
	private String siglaPais;
	/**
	 * @return the codigoPais
	 */
	public Integer getCodigoPais() {
		return codigoPais;
	}
	/**
	 * @param codigoPais the codigoPais to set
	 */
	public void setCodigoPais(Integer codigoPais) {
		this.codigoPais = codigoPais;
	}
	/**
	 * @return the descricaoPais
	 */
	public String getDescricaoPais() {
		return descricaoPais;
	}
	/**
	 * @param descricaoPais the descricaoPais to set
	 */
	public void setDescricaoPais(String descricaoPais) {
		this.descricaoPais = descricaoPais;
	}
	/**
	 * @return the siglaPais
	 */
	public String getSiglaPais() {
		return siglaPais;
	}
	/**
	 * @param siglaPais the siglaPais to set
	 */
	public void setSiglaPais(String siglaPais) {
		this.siglaPais = siglaPais;
	}
	
	
}
