package br.tcc.uni7.entidades;

public class LogradouroEntidade {
	private Integer codigoLogradouro;
	private Integer codigoCidadeLogradouro;
	private String descricaoLogradouro;
	/**
	 * @return the codigoLogradouro
	 */
	public Integer getCodigoLogradouro() {
		return codigoLogradouro;
	}
	/**
	 * @param codigoLogradouro the codigoLogradouro to set
	 */
	public void setCodigoLogradouro(Integer codigoLogradouro) {
		this.codigoLogradouro = codigoLogradouro;
	}
	/**
	 * @return the codigoCidadeLogradouro
	 */
	public Integer getCodigoCidadeLogradouro() {
		return codigoCidadeLogradouro;
	}
	/**
	 * @param codigoCidadeLogradouro the codigoCidadeLogradouro to set
	 */
	public void setCodigoCidadeLogradouro(Integer codigoCidadeLogradouro) {
		this.codigoCidadeLogradouro = codigoCidadeLogradouro;
	}
	/**
	 * @return the descricaoLogradouro
	 */
	public String getDescricaoLogradouro() {
		return descricaoLogradouro;
	}
	/**
	 * @param descricaoLogradouro the descricaoLogradouro to set
	 */
	public void setDescricaoLogradouro(String descricaoLogradouro) {
		this.descricaoLogradouro = descricaoLogradouro;
	}
	
	

}
